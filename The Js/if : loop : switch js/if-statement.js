// if statement

var firstName = "john";
var civilStatus = "single";

if (civilStatus === "married") {
  console.log(firstName + " " + "is married!");
} else {
  console.log(firstName + " " + "is not married!");
}

var isMarried = true;
// can pass true or false statement into the if argument
if (isMarried) {
  console.log(firstName + " " + "is married!");
} else {
  console.log(firstName + " " + "is not married!");
}
